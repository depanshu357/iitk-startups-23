import requests
import os

def download_images(image_links, output_folder):
    if not os.path.exists(output_folder):
        os.makedirs(output_folder)
    
    for index, link in enumerate(image_links, start=1):
        try:
            response = requests.get(link)
            if response.status_code == 200:
                with open(os.path.join(output_folder, f"image_{index}.jpg"), 'wb') as f:
                    f.write(response.content)
                print(f"Downloaded image {index}/{len(image_links)}")
            else:
                print(f"Failed to download image {index}")
        except Exception as e:
            print(f"Error downloading image {index}: {str(e)}")

